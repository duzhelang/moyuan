influence = """文学
诗作
苏轼对社会的看法和对人生的思考都毫无掩饰地表现在其文学作品中，其中又以诗歌最为淋漓酣畅。在二千七百多首苏诗中，干预社会现实和思考人生的题材十分突出。
苏轼对社会现实中种种不合理的现象抱着"一肚皮不入时宜"的态度，始终把批判现实作为诗歌的重要主题。
苏轼一生宦海浮沉，奔走四方，生活阅历极为丰富。他善于从人生遭遇中总结经验，也善于从客观事物中见出规律。在他眼中，极平常的生活内容和自然景物都蕴含着深刻的道理。

词作
苏轼在词的创作上取得了非凡的成就，就一种文体自身的发展而言，苏词的历史性贡献又超过了苏文和苏诗。苏轼继柳永之后，对词体进行了全面的改革，最终突破了词为"艳科"的传统格局，提高了词的文学地位。
苏轼对词的变革，基于他诗词一体的词学观念和"自成一家"的创作主张。扩大词的表现功能，开拓词境，是苏轼改革词体的主要方向。

文章
苏轼的文学思想是文、道并重。他推崇韩愈和欧阳修对古文的贡献，都是兼从文、道两方面着眼的。苏轼的散文呈现出多姿多彩的艺术风貌。

艺术
书法
苏轼擅长写行书、楷书，与黄庭坚、米芾、蔡襄并称为"宋四家"。

绘画
苏轼擅长画墨竹，且绘画重视神似，主张画外有情，画要有寄托，反对形似，提倡"诗画本一律，天工与清新"。"""

