life_story = """早年经历
苏轼于宋景祐三年十二月十九日（1037年1月8日）出生于眉州眉山，是初唐大臣苏味道之后裔。苏轼之名"轼"原意为车前的扶手，取其默默无闻却不可或缺之意。苏轼幼年承受良好的家教，其父苏洵是古文名家，其母程氏曾以东汉名士范滂的事迹勉励其砥砺名节。
庆历八年（1048年），苏洵因父丧居家，闭户读书，把自己的学识品行教授给苏轼与幼子苏辙。
苏轼生性放达，为人率真，深得道家风范。好交友、好美食，创造许多饮食精品，好品茗，亦雅好游山林。

进京应试
嘉祐元年（1056年），苏轼首次出川赴京，参加朝廷的科举考试。苏洵带着二十一岁的苏轼、十九岁的苏辙，自偏僻的西蜀地区，沿江东下，于嘉祐二年（1057年）进京应试。
当年考试的策论题目是《刑赏忠厚之至论》。苏轼的策论获得当时的主考官欧阳修和小试官梅尧臣的赏识。欧阳修不禁对苏轼的豪迈极为欣赏，而且预见了苏轼的将来："此人可谓善读书，善用书，他日文章必独步天下。"

名动京师
在欧阳修的一再称赞下，苏轼一时声名大噪。他每有新作，立刻就会传遍京师。
嘉祐六年（1061年），在欧阳修的推荐下，苏轼兄弟参加名为"贤良方正能直言极谏科"的制科考试。苏轼的对策被评为第三等，被称为"百年第一"。

乌台诗案
元丰二年（1079年）四月，苏轼调任湖州知州。因诗文被新党利用，七月二十八日被御史台的吏卒逮捕，解往京师。这就是北宋著名的"乌台诗案"。
苏轼得到从轻发落，贬为黄州团练副使。公务之余，他带领家人开垦城东的一块坡地，种田帮补生计。"东坡居士"的别号便是苏轼在这时起的。

东山再起
元丰八年（1085年），宋哲宗即位，苏轼复为朝奉郎知登州。四个月后，以礼部郎中被召还朝。在朝半月，升为起居舍人。三个月后，升中书舍人。不久，再又升翰林学士、知制诰。

再任杭州
元祐四年（1089年），苏轼任龙图阁学士、知杭州。苏轼第二次到杭州做官，杭州大旱，饥馑瘟疫一起发生。苏轼向朝廷请求，减免本路上供米三分之一，又得到赐给剃度和尚的度牒，换成大米来救济饥饿的百姓。长堤筑成，在堤上种植芙蓉、杨柳，望去好像图画，杭州人把长堤命名为苏公堤。

流落儋州
绍圣四年（1097年），苏轼被一叶孤舟送到了徼边荒凉之地海南岛儋州。他把儋州当成了自己的第二故乡，"我本儋耳氏，寄生西蜀州"。他在这里办学堂，介学风，以致许多人不远千里，追至儋州，从苏轼学。

身逝常州
建中靖国元年七月二十八日（1101年8月24日）在常州逝世。宋高宗即位后，追赠苏轼为太师；宋孝宗时，追谥"文忠"。"""

